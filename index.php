<p>
  Walter Elias „Walt“ Disney (5. prosince 1901, Hermosa, Chicago, Illinois, USA – 15. prosince 1966, Burbank, Kalifornie, USA) byl americký filmový producent, režisér, scenárista, dabér, animátor, podnikatel a filantrop. Ve své době silně ovlivnil zábavní průmysl. Jako zakladatel (společně se svým bratrem Royem O. Disneyem) firmy Walt Disney Productions se stal jedním z nejznámějších filmových producentů na světě. Společnost, kterou založil, se v současnosti jmenuje The Walt Disney Company a má roční obrat asi 35 miliard dolarů.

  Znám je jako filmový producent a bavič, zároveň však také jako inovátor animovaného filmu a zábavních parků. Společně se zaměstnanci společnosti vytvořil řadu světově známých postaviček včetně Mickey Mouse, kterému Disney propůjčil i vlastní hlas. Vyhrál 26 Oskarů z 59 nominací, včetně rekordních čtyř Oskarů v jednom roce,[2] čímž v tomto oboru získal více cen a nominací než kdokoli jiný. Také obdržel sedm ocenění Emmy. Po něm pojmenované zábavní parky můžeme najít ve Spojených státech amerických (Disneyland, Walt Disney World Resort), v Japonsku (Tokyo Disney Resort), Francii (Disneyland Paris) a Číně (Hong Kong Disneyland). Zemřel na rakovinu plic v kalifornském Burbanku.
</p>


